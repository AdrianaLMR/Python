#Programa que lê dois números e mostre a soma entre eles utilizando os tipos primitivos(incompleto)
print('Somando dois números')
number1 = int(input('Digite um número'))
number2 = int(input('Digite o segundo número'))
somar = number1 + number2

print('A soma entre {} e {} é igual a: {}'.format(number1, number2, somar))

print('Subtraindo dois números')
number3 = int(input('Digite um número'))
number4 = int(input('Digite o segundo número'))
subtrair = number3 - number4

print('A subtração entre {} e {} é igual a: {}'.format(number3, number4, subtrair))

print('Dividindo dois números')
number5 = float(input('Digite um número'))
number6 = float(input('Digite o segundo número'))
dividir = number5 / number6

print('A divisão  entre {} e {} é igual a: {}'.format(number5, number6, dividir))

print('Multiplicando dois números')
number7 = int(input('Digite um número'))
number8 = int(input('Digite o segundo número'))
multiplicar = number7 * number8

print('A multiplicação entre {} e {} é igual a: {}'.format(number7, number8, multiplicar))

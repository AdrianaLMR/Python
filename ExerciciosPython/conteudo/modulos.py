# Módulos em Python são arquivos que contêm definições e implementações de funções, classes e variáveis, e que
# podem ser importados e usados em outros programas.

# Eles permitem organizar o código em partes menores e promovem a reutilização e facilitam a manutenção.

# Um módulo é simplesmente um arquivo Python com a extensão .py.

# Para usar as funções e variáveis de um módulo em outro arquivo Python, você deve importá-lo.
# --> Import   name-modulo   importa_todo_modulo
# --> 'from name-modulo import um-dado-modulo sqrt, pow'

# Você pode importar funções específicas de um módulo usando a palavra-chave "from".

# Você pode usar 'as' para dar um nome diferente ao módulo ou função ao importá-lo.

# Você pode importar tudo de um módulo usando "*", mas isso não é recomendado, pois pode causar conflitos de nome.

# Python vem com uma biblioteca padrão rica em módulos. Por exemplo, você pode usar o módulo 'math' para funções matemáticas.


# Você pode instalar módulos de terceiros usando o 'pip', que é o gerenciador de pacotes do Python


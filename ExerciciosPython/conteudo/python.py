# Python

# É uma linguagem de programação de alto nível, interpretada e de propósito geral, conhecida por sua simplicidade e
# legibilidade. Criada por Guido van Rossum e lançada pela primeira vez em 1991, Python se destaca por sua sintaxe clara,
# que promove um código mais legível e conciso.

# --> Indentação: Python usa indentação para definir blocos de código, ao invés de chaves {} ou palavras-chave como end.
# A indentação correta é crucial e erros de indentação resultam em erros de sintaxe.
if True:
    print("Indentação é importante!")

# --> Declarações Simples: As declarações em Python geralmente são simples e diretas, promovendo a clareza.
nome = "Python"
print("Olá,{}".format(nome))

# --> Estruturas de Controle: Estruturas de controle comuns como loops e condicionais, com uma sintaxe clara.
x = 2
for i in range(5):
    print(i)

if x > 0:
    print("Positivo")
else:
    print("Não positivo")

# Características

# --> Interpretada: Python é uma linguagem interpretada, o que significa que o código é executado linha a linha.
# Isso facilita a depuração e torna o desenvolvimento mais rápido.

# --> Tipagem Dinâmica: Python é dinamicamente tipada, ou seja, você não precisa declarar o tipo de uma variável.
# O tipo é determinado em tempo de execução.

# --> Multiparadigma: Python suporta vários paradigmas de programação, incluindo programação imperativa, orientada a
# objetos e funcional.

# --> Biblioteca Padrão Extensa: Python vem com uma biblioteca padrão rica que fornece módulos e pacotes para uma ampla
# variedade de tarefas, desde manipulação de arquivos até chamadas de rede.

# --> Comunidade Ativa: Python possui uma comunidade ativa e crescente que contribui com uma vasta gama de bibliotecas e
# frameworks.

# Utilidades

# --> Python é extremamente versátil e pode ser usado em várias áreas, incluindo:

# --> Desenvolvimento Web: Frameworks como Django e Flask facilitam a criação de aplicações web robustas.

# --> Ciência de Dados e Análise: Bibliotecas como Pandas, NumPy e SciPy são amplamente utilizadas para análise de dados,
# enquanto o Matplotlib e o Seaborn são usados para visualização.

# --> Inteligência Artificial e Machine Learning: Python é a linguagem preferida para IA e ML, com bibliotecas poderosas como
# TensorFlow, Keras e PyTorch.

# --> Automação: Scripts em Python são frequentemente usados para automatizar tarefas repetitivas.

# --> Desenvolvimento de Software: Python é usado para prototipagem rápida e desenvolvimento de software, com suporte para
# testes e integração contínua.

# --> Aplicações Desktop: Bibliotecas como Tkinter e PyQt permitem a criação de aplicações GUI.


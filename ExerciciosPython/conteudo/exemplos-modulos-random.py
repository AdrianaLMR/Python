import random

# Número real aleatório
num = random.random()
print('Número real aleatório {}'.format(num))

# Número inteiro aleatório
numero = random.randint(1, 10)
print('Número inteiro aleatório {}'.format(numero))

# Número float aleatório
valor = random.uniform(1, 10)
print('Número float aleatório {}'.format(valor))


